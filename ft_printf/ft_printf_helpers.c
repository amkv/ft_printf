#include "../libftprintf.h"

int			ft_numlen(int num)
{
	int		len;

	len = 0;
	if (num < 0)
	{
		len++;
		num = -num;
	}
	while (num != 0)
	{
		num /= 10;
		len++;
	}
	return (len);
}
